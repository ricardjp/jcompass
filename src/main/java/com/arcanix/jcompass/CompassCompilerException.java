package com.arcanix.jcompass;

/**
 * @author ricardjp@arcanix.com (Jean-Philippe Ricard)
 */
public class CompassCompilerException extends Exception {

    public CompassCompilerException() {
        super();
    }

    public CompassCompilerException(String message) {
        super(message);
    }

    public CompassCompilerException(Throwable cause) {
        super(cause);
    }

    public CompassCompilerException(String message, Throwable cause) {
        super(message, cause);
    }

}
